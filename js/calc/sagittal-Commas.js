/**
 * Return the comma size in cents for a given sagittal key (positive or negative).
 * Mirrors the Excel formula: =IF(key>0, VLOOKUP(key,…,5,FALSE), -1*VLOOKUP(-key,…,5,FALSE))
 */
export function getCommaCents(key) {
    if (key === null || key === undefined || key === '') return 0;
    const absKey = Math.abs(Number(key));
    const entry  = COMMAS[absKey];
    if (!entry) return 0;
    return Number(key) < 0 ? -entry.cents : entry.cents;
}

